    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
double lado1 = Double.parseDouble(txtLado1.getText());
double lado2 = Double.parseDouble(txtLado2.getText());
double lado3 = Double.parseDouble(txtLado3.getText());

if (lado1 == lado2 && lado1 == lado3){
    lblResposta.setText("Triangulo Equil√°tereo");
}
else if  (lado1 == lado2 && lado1 != lado3
    || lado2 == lado3 && lado3 != lado1
    || lado3 == lado1 && lado3 != lado2){
    lblResposta.setText("Triangulo Isosceles");
        
}

else {
    lblResposta.setText("TrianguloEscaleno");
}
    }//GEN-LAST:event_btnVerificarActionPerformed

